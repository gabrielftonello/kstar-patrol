    call desenha_delimitador
    pop si
    pop di
    mov bx, di
    pop cx
    loop desenha_botoes_loop



ret
endp
    
   tela_inicial proc
 
inicio_tela_inicial:
    
    mov bl,2
    xor dx, dx
    mov bp, offset logo_nome_jogo
    call escreve_string
    
    mov bl, 0Ch
    mov si, offset configuracao_tela_inicial
    mov di, 0Fh

    call desenha_botoes
    
    mov di, 34565
    mov si, offset desenho_nave_aliada
    call desenha_nave_menu
    mov nave_menu_flag, 0
    
ler_key_loop:
    
    push bx
    call move_nave_menu
    pop bx
 
    call interrompe_programa
  
    mov ah,01h
    int 16h
    jz ler_key_loop

    call ler_key
    cmp ah,72
    jz up_arrow_key_tela_inicial
    cmp ah,80
    jz down_arrow_key_tela_inicial
    cmp ah, 28
    jz enter_key
    
    
up_arrow_key_tela_inicial:
    mov bl, 0Ch
    mov si, offset configuracao_tela_inicial
    mov di, 0Fh
    call desenha_botoes
    mov selecionado, 0
    jmp ler_key_loop
    
down_arrow_key_tela_inicial:
    
    mov bl, 0Fh
    mov si, offset configuracao_tela_inicial
    mov di, 0Ch
    call desenha_botoes
    mov selecionado, 1
    jmp ler_key_loop
    
enter_key:
    cmp selecionado, 0
    mov naves_imunes, 0
    jnz fim_tela_inicial
    call iniciar_jogo
    call remover_exibicao
    jmp inicio_tela_inicial

fim_tela_inicial:

    ret
    endp
    
 desenha_delimitador proc

    mov cx, 8
    
    mov si, offset configuracao_delimitador
loop_delimitador:
    call posiciona_cursor
    mov di, cx
    mov al, [si]
    inc si

    mov cx, [si]
    xor ch, ch
    call escreve_caracter
    inc si
    mov cx, di
    add dl, [si]
    inc si
    add dh, [si]
    inc si
 loop loop_delimitador
 
    ret
endp


